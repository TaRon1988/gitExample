package daily_Test;

public class BreakEx4 {

	public static void main(String[] args) {
		int i =0;
		System.out.println("------------------");
		while(true) {
			if(i>=8) break;//반복문을 종료시킴
			++i;
			System.out.println(i+" Hello World");
		}

		System.out.println("------------------");	}

}
